# loop through string characters
for letter in "Giraffe":
    print(letter)

# loop through a list
shopping = ["grapes", "pineapples", "milk"]
for item in shopping:
    print(item)

# loop through a range of numbers, excluding the upper range limit
for index in range(7,11):
    print(index)

# range function can take one parameter only, default is starting at zero
for j in range(len(shopping)):
    print(shopping[j])